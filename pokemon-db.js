function getPokemonSpriteUrl(poke) {
  const BASE = 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon';
  if (poke.isMega) {
    const fid = MEGA_SPRITE_IDS[String(poke.id)];
    if (fid) return `${BASE}/other/official-artwork/${fid}.png`;
    return `${BASE}/${parseInt(String(poke.id).replace('mega-', ''))}.png`;
  }
  return `${BASE}/other/official-artwork/${poke.id}.png`;
}
